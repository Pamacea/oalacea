// Generate blog article draft
async function generateBlogDraft(
  title: string,
  options: GenerationRequest['options'] = {}
): Promise<string> {
  const tone = options?.tone || 'professional';
  const language = options?.language || 'en';
  const maxLength = options?.maxLength || 1000;

  const systemPrompt = `You are a professional content writer for a tech blog. Generate well-structured blog posts with proper headings, bullet points, and engaging content. ${language === 'fr' ? 'Write in French.' : 'Write in English.'}`;

  const prompt = `Write a blog post draft with the title "${title}".
Tone: ${tone}
Target length: ~${maxLength} words
${options?.keywords ? `Keywords to include: ${options.keywords.join(', ')}` : ''}
${options?.context ? `Additional context: ${options.context}` : ''}

Include:
- An engaging introduction
- 3-5 main sections with headings
- Code examples if technical
- A conclusion

IMPORTANT: Add a note at the end that this is AI-generated and requires human review.`;

  return generateContent(prompt, systemPrompt);
}

// Generate project description
async function generateProjectDescription(
  projectName: string,
  options: GenerationRequest['options'] = {}
): Promise<string> {
  const systemPrompt = 'You write compelling project descriptions for portfolio sites. Focus on technical achievements and user value.';

  const prompt = `Write a project description for "${projectName}".
${options?.context ? `Context: ${options.context}` : ''}
${options?.tone ? `Tone: ${options.tone}` : ''}

Include:
- One-line summary
- Technical stack used
- Key features (3-5 bullet points)
- Challenges overcome

Keep it under 200 words. Add note that this is AI-generated.`;

  return generateContent(prompt, systemPrompt);
}

// Translate content
async function translateContent(
  content: string,
  targetLanguage: 'en' | 'fr'
): Promise<string> {
  const systemPrompt = `You are a professional translator. Translate content while preserving meaning, tone, and formatting. ${targetLanguage === 'fr' ? 'Translate to French.' : 'Translate to English.'}`;

  const prompt = `Translate the following content. Preserve markdown formatting, code blocks, and structure.

Content to translate:
${content}

IMPORTANT: Add a note at the end that this is a machine translation and requires human review.`;

  return generateContent(prompt, systemPrompt);
}

// Generate alt text for images
async function generateAltText(imageDescription: string): Promise<string> {
  const systemPrompt = 'Generate concise, descriptive alt text for images following accessibility best practices. Maximum 125 characters.';

  const prompt = `Generate alt text for an image described as: "${imageDescription}"

The alt text should be:
- Descriptive but concise
- Under 125 characters
- Focused on important visual information
- Useful for screen reader users`;

  return generateContent(prompt, systemPrompt);
}

// Generate image using DALL-E or similar
async function generateImage(prompt: string): Promise<string> {
  const config = getAIConfig();

  if (config.provider === 'openai' && config.apiKey) {
    const response = await fetch('https://api.openai.com/v1/images/generations', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${config.apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'dall-e-3',
        prompt: `${prompt}. Minimalist, professional style suitable for a tech blog.`,
        n: 1,
        size: '1024x1024',
        response_format: 'url',
      }),
    });

    if (!response.ok) {
      throw new Error(`Image generation error: ${response.statusText}`);
    }

    const data = await response.json();
    return data.data[0]?.url || '';
  }

  if (config.provider === 'ollama' && config.apiUrl) {
    // Use Stable Diffusion via Ollama if available
    try {
      const response = await fetch(`${config.apiUrl}/api/generate`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          model: 'stablediffusion',
          prompt,
        }),
      });

      if (response.ok) {
        const data = await response.json();
        return data.image || '';
      }
    } catch {
      // Fall through to mock
    }
  }

  // Mock image URL
  return `data:image/svg+xml,${encodeURIComponent(`
    <svg width="1024" height="1024" xmlns="http://www.w3.org/2000/svg">
      <rect width="1024" height="1024" fill="#1a1a2e"/>
      <text x="512" y="512" font-family="Arial" font-size="48" fill="#ffffff" text-anchor="middle">
        AI Image Placeholder
      </text>
      <text x="512" y="580" font-family="Arial" font-size="24" fill="#888888" text-anchor="middle">
        ${prompt.substring(0, 50)}...
      </text>
    </svg>
  `)}`;
}

// Main content generation function
async function generateContent(prompt: string, systemPrompt?: string): Promise<string> {
  const config = getAIConfig();

  switch (config.provider) {
    case 'openai':
      return generateWithOpenAI(prompt, config, systemPrompt);
    case 'anthropic':
      return generateWithAnthropic(prompt, config, systemPrompt);
    case 'ollama':
      return generateWithOllama(`${systemPrompt || ''}\n\n${prompt}`, config);
    case 'mock':
      return generateMock('default', prompt);
    default:
      throw new Error('No AI provider configured');
  }
}

// POST handler for generation requests
export async function POST(request: NextRequest) {
  try {
    const body: GenerationRequest = await request.json();
    const { type, input, options } = body;

    if (!type || !input) {
      return NextResponse.json(
        { success: false, error: 'Missing required fields: type and input' },
        { status: 400 }
      );
    }

    let content: string | undefined;
    let imageUrl: string | undefined;
    const config = getAIConfig();

    switch (type) {
      case 'blog-draft':
        content = await generateBlogDraft(input, options);
        break;

      case 'project-description':
        content = await generateProjectDescription(input, options);
        break;

      case 'translate':
        content = await translateContent(input, options?.language || 'en');
        break;

      case 'alt-text':
        content = await generateAltText(input);
        break;

      case 'image':
        imageUrl = await generateImage(input);
        break;

      default:
        return NextResponse.json(
          { success: false, error: `Unknown generation type: ${type}` },
          { status: 400 }
        );
    }

    const response: GenerationResponse = {
      success: true,
      content,
      imageUrl,
      provider: config.provider,
      requiresReview: true,
    };

    return NextResponse.json(response);
  } catch (error) {
    console.error('[AI Generate] Error:', error);
    return NextResponse.json(
      {
        success: false,
        error: error instanceof Error ? error.message : 'Unknown error',
        provider: getAIConfig().provider,
        requiresReview: true,
      } as GenerationResponse,
      { status: 500 }
    );
  }
}

// GET handler for provider info
export async function GET() {
  const config = getAIConfig();

  return NextResponse.json({
    provider: config.provider,
    model: config.model,
    available: config.provider !== 'mock',
    requiresReview: true,
    capabilities: {
      'blog-draft': true,
      'project-description': true,
      'translate': true,
      'alt-text': true,
      'image': config.provider === 'openai' || config.provider === 'mock',
    },
  });
}
