// AI Content Generation API - EXPERIMENTAL
// Generates blog drafts, descriptions, translations, alt text, and images

import { NextRequest, NextResponse } from 'next/server';
import { revalidatePath } from 'next/cache';

export const runtime = 'nodejs';
export const dynamic = 'force-dynamic';

// AI Provider types
type AIProvider = 'openai' | 'anthropic' | 'ollama' | 'mock';

interface AIConfig {
  provider: AIProvider;
  apiKey?: string;
  apiUrl?: string;
  model?: string;
}

interface GenerationRequest {
  type: 'blog-draft' | 'project-description' | 'translate' | 'alt-text' | 'image';
  input: string;
  options?: {
    language?: 'en' | 'fr';
    tone?: 'professional' | 'casual' | 'technical' | 'creative';
    maxLength?: number;
    keywords?: string[];
    title?: string;
    context?: string;
  };
}

interface GenerationResponse {
  success: boolean;
  content?: string;
  imageUrl?: string;
  error?: string;
  provider: AIProvider;
  requiresReview: true;
}

// Get AI configuration from environment
function getAIConfig(): AIConfig {
  if (process.env.OPENAI_API_KEY) {
    return {
      provider: 'openai',
      apiKey: process.env.OPENAI_API_KEY,
      model: process.env.OPENAI_MODEL || 'gpt-4o-mini',
    };
  }

  if (process.env.ANTHROPIC_API_KEY) {
    return {
      provider: 'anthropic',
      apiKey: process.env.ANTHROPIC_API_KEY,
      model: process.env.ANTHROPIC_MODEL || 'claude-3-haiku-20240307',
    };
  }

  if (process.env.OLLAMA_API_URL) {
    return {
      provider: 'ollama',
      apiUrl: process.env.OLLAMA_API_URL,
      model: process.env.OLLAMA_MODEL || 'llama2',
    };
  }

  // Fallback to mock for development
  return { provider: 'mock' };
}

// OpenAI generation
async function generateWithOpenAI(
  prompt: string,
  config: AIConfig,
  systemPrompt?: string
): Promise<string> {
  const response = await fetch('https://api.openai.com/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${config.apiKey}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      model: config.model || 'gpt-4o-mini',
      messages: [
        { role: 'system', content: systemPrompt || 'You are a helpful assistant.' },
        { role: 'user', content: prompt },
      ],
      max_tokens: 2000,
      temperature: 0.7,
    }),
  });

  if (!response.ok) {
    throw new Error(`OpenAI error: ${response.statusText}`);
  }

  const data = await response.json();
  return data.choices[0]?.message?.content || '';
}

// Anthropic generation
async function generateWithAnthropic(
  prompt: string,
  config: AIConfig,
  systemPrompt?: string
): Promise<string> {
  const response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'x-api-key': config.apiKey!,
      'Content-Type': 'application/json',
      'anthropic-version': '2023-06-01',
    },
    body: JSON.stringify({
      model: config.model || 'claude-3-haiku-20240307',
      max_tokens: 2000,
      system: systemPrompt || 'You are a helpful assistant.',
      messages: [{ role: 'user', content: prompt }],
    }),
  });

  if (!response.ok) {
    throw new Error(`Anthropic error: ${response.statusText}`);
  }

  const data = await response.json();
  return data.content[0]?.text || '';
}

// Ollama generation (local)
async function generateWithOllama(
  prompt: string,
  config: AIConfig
): Promise<string> {
  const response = await fetch(`${config.apiUrl}/api/generate`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      model: config.model || 'llama2',
      prompt,
      stream: false,
    }),
  });

  if (!response.ok) {
    throw new Error(`Ollama error: ${response.statusText}`);
  }

  const data = await response.json();
  return data.response || '';
}

